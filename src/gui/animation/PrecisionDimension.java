package gui.animation;

public class PrecisionDimension {
	public double width;
	public double height;
	
	public PrecisionDimension(double width, double height) {
		this.width = width;
		this.height = height;
	}
	
	public PrecisionDimension() {
		this.width = 0;
		this.height = 0;
	}
}
